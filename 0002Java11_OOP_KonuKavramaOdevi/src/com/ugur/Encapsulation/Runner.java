package com.ugur.Encapsulation;

public class Runner {
	/**
	 * Kapsülleme (encapsulation), nesne yönelimli programlamada önemli bir
	 * prensiptir ve verilerin (özellikler) ve bu verilere erişim metotlarının bir
	 * arada gruplandırılmasıdır. Kapsülleme, verilere doğrudan erişimi kısıtlayarak
	 * veri bütünlüğünü ve güvenliğini sağlar. Bu sayede, veriler istenmeyen
	 * durumlardan korunabilir ve daha düzenli bir program tasarımı elde edilebilir.
	 * 
	 * Kapsülleme neden önemlidir:
	 * 
	 * Veri Güvenliği: Doğrudan erişim yerine erişim metotları kullanarak, verilere
	 * sadece kontrollü bir şekilde erişilebilir. Bu durum, verilerin istenmeyen
	 * değişikliklere karşı korunmasını sağlar.
	 * 
	 * Kod Bakımı: Eğer verilerin erişimi ve değişimi sadece belirli metotlar
	 * üzerinden yapılıyorsa, ilgili metotları güncellemek veya değiştirmek daha
	 * kolaydır. Bu, kod bakımını kolaylaştırır.
	 * 
	 * Esneklik: Erişim metotları sayesinde verilerin iç yapısı değiştiğinde bile
	 * dışarıdaki kodlar etkilenmez. Bu, kodun daha esnek ve genişletilebilir
	 * olmasını sağlar.
	 * 
	 * Kod Kontrolü: Hangi verilerin ne zaman ve nasıl değiştirildiğini kontrol
	 * etmek daha kolaydır. Bu, hataları tespit etmeyi ve düzeltmeyi kolaylaştırır.
	 * 
	 * Java'da kapsüllemeyi uygulamak için, sınıfın özelliklerini private (özel)
	 * erişim belirteci ile tanımlayıp, bu özelliklere erişim sağlayan public
	 * (genel) erişim belirteciyle metotlar oluşturabiliriz. Bu metotlar, veriye
	 * erişimi kontrol eder ve gerektiğinde işlem yapabilir.
	 */

}
